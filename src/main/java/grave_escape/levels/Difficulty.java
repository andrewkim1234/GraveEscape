package grave_escape.levels;

/**
 * The Difficulty enum represents the three possible difficulties a player can select in the game menus. Depending on
 * the difficulty selected, the level will include more enemies and objectives.
 */
public enum Difficulty {
    /**
     * Represents the Easy difficulty
     */
    EASY,
    /**
     * Represents the Normal difficulty
     */
    NORMAL,
    /**
     * Represents the Hard difficulty
     */
    HARD
}
