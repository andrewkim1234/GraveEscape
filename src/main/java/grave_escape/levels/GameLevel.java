package grave_escape.levels;

/**
 * The GameLevel enum represents the three possible levels a player can select in the game menus. Depending on
 * the level selected, the level will deviate in appearance and layout.
 */
public enum GameLevel {
    /**
     * Represents Level 1
     */
    Level1,
    /**
     * Represents Level 2
     */
    Level2,
    /**
     * Represents Level 3
     */
    Level3
}
