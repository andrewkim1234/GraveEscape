package grave_escape.game;

/**
 * Represents the possible movement directions in the game.
 */
public enum Direction {
    /**
     * Direction representing upward movement.
     */
    UP,

    /**
     * Direction representing downward movement.
     */
    DOWN,

    /**
     * Direction representing leftward movement.
     */
    LEFT,

    /**
     * Direction representing rightward movement.
     */
    RIGHT,

    /**
     * Represents an invalid or unspecified direction.
     */
    INVALID
}
